"""
Compare baseline vs verification-gated results for CLEAN false-alarm experiments.

Designed for metric files generated by:
    evaluation_event_trigger_clean.py

Main goals:
1) Check whether baseline and gated results are truly comparable
2) Print a clean-summary table suitable for paper writing
3) Quantify relative reduction in useless MTD burden

Author: ChatGPT (for DDET-MTD clean experiment)
"""

from __future__ import annotations

import argparse
import math
import os
from datetime import datetime
from typing import Any, Dict, List, Tuple

import numpy as np


# =========================
# Default file paths
# =========================
DEFAULT_BASELINE = "metric/case14/metric_event_trigger_clean_tau_-1.0_mode_0_0.03_1.1.npy"
DEFAULT_GATED = "metric/case14/metric_event_trigger_clean_tau_0.021_mode_0_0.03_1.1.npy"


# =========================
# Helpers
# =========================
def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Compare clean false-alarm baseline vs gated metric files."
    )
    parser.add_argument("--baseline", type=str, default=DEFAULT_BASELINE,
                        help="Path to baseline clean metric file (.npy)")
    parser.add_argument("--gated", type=str, default=DEFAULT_GATED,
                        help="Path to gated clean metric file (.npy)")
    parser.add_argument("--rtol", type=float, default=1e-8,
                        help="Relative tolerance for numeric list comparison")
    parser.add_argument("--atol", type=float, default=1e-10,
                        help="Absolute tolerance for numeric list comparison")
    return parser.parse_args()


def fmt_mtime(path: str) -> str:
    ts = os.path.getmtime(path)
    return datetime.fromtimestamp(ts).strftime("%Y-%m-%d %H:%M:%S")


def load_result(path: str) -> Dict[str, Any]:
    if not os.path.exists(path):
        raise FileNotFoundError(f"File not found: {path}")
    data = np.load(path, allow_pickle=True).item()
    if not isinstance(data, dict):
        raise TypeError(f"Loaded object is not a dict: {type(data)}")
    return data


def detect_group_key(data: Dict[str, Any]) -> str:
    """
    Clean script uses a single group key, typically '(0,0.0)'.
    We auto-detect it from the most reliable dict fields.
    """
    candidate_fields = [
        "total_clean_sample",
        "total_DDD_alarm",
        "TP_DDD",
        "verify_score",
        "trigger_after_verification",
    ]
    for field in candidate_fields:
        if field in data and isinstance(data[field], dict) and len(data[field]) > 0:
            keys = list(data[field].keys())
            if len(keys) == 1:
                return keys[0]
            elif "(0,0.0)" in keys:
                return "(0,0.0)"
            else:
                return keys[0]
    raise KeyError("Cannot detect group_key from metric file.")


def get_scalar_from_group_dict(
    data: Dict[str, Any],
    field: str,
    group_key: str,
    default: float = float("nan")
) -> float:
    value = data.get(field, None)
    if isinstance(value, dict):
        if group_key in value:
            return value[group_key]
    return default


def get_list_from_group_dict(
    data: Dict[str, Any],
    field: str,
    group_key: str
) -> List[Any]:
    value = data.get(field, None)
    if isinstance(value, dict):
        return value.get(group_key, [])
    return []


def safe_float(x: Any) -> float:
    try:
        return float(x)
    except Exception:
        return float("nan")


def safe_int(x: Any) -> int:
    try:
        return int(x)
    except Exception:
        return -1


def approx_equal_scalar(a: Any, b: Any, rtol: float, atol: float) -> bool:
    try:
        a = float(a)
        b = float(b)
        if math.isnan(a) and math.isnan(b):
            return True
        return math.isclose(a, b, rel_tol=rtol, abs_tol=atol)
    except Exception:
        return a == b


def compare_lists(
    a: List[Any],
    b: List[Any],
    rtol: float,
    atol: float
) -> Tuple[bool, str]:
    """
    Robust list comparison:
    - exact equality for bool/int/str
    - allclose for numeric values
    """
    if len(a) != len(b):
        return False, f"length mismatch: {len(a)} vs {len(b)}"

    if len(a) == 0:
        return True, "both empty"

    # Try numeric comparison
    numeric = True
    aa = []
    bb = []
    for x, y in zip(a, b):
        try:
            aa.append(float(x))
            bb.append(float(y))
        except Exception:
            numeric = False
            break

    if numeric:
        aa = np.asarray(aa, dtype=float)
        bb = np.asarray(bb, dtype=float)
        same = np.allclose(aa, bb, rtol=rtol, atol=atol, equal_nan=True)
        if same:
            return True, "allclose"
        diff_pos = np.where(~np.isclose(aa, bb, rtol=rtol, atol=atol, equal_nan=True))[0]
        first = int(diff_pos[0])
        return False, f"first numeric mismatch at pos={first}: {aa[first]} vs {bb[first]}"

    # Fallback exact comparison
    for i, (x, y) in enumerate(zip(a, b)):
        if x != y:
            return False, f"first mismatch at pos={i}: {x} vs {y}"

    return True, "exact match"


def pct_reduction(baseline: float, gated: float) -> float:
    """
    Positive means gated is smaller than baseline.
    Example: baseline=10, gated=4 => +60%
    """
    baseline = float(baseline)
    gated = float(gated)
    if math.isnan(baseline) or math.isnan(gated):
        return float("nan")
    if abs(baseline) < 1e-15:
        return float("nan")
    return (baseline - gated) / baseline * 100.0


def pct_change(baseline: float, gated: float) -> float:
    """
    Standard percentage change from baseline to gated:
    (gated - baseline) / baseline * 100
    """
    baseline = float(baseline)
    gated = float(gated)
    if math.isnan(baseline) or math.isnan(gated):
        return float("nan")
    if abs(baseline) < 1e-15:
        return float("nan")
    return (gated - baseline) / baseline * 100.0


def fmt_num(x: Any, digits: int = 6) -> str:
    try:
        x = float(x)
        if math.isnan(x):
            return "nan"
        return f"{x:.{digits}f}"
    except Exception:
        return str(x)


def print_header(title: str) -> None:
    print("\n" + "=" * 100)
    print(title)
    print("=" * 100)


def summarize(data: Dict[str, Any], group_key: str) -> Dict[str, Any]:
    summary = {}

    # Meta
    summary["group_key"] = group_key
    summary["tau_verify"] = data.get("tau_verify", float("nan"))

    # Counts
    summary["total_clean_sample"] = safe_int(get_scalar_from_group_dict(data, "total_clean_sample", group_key))
    summary["total_DDD_alarm"] = safe_int(get_scalar_from_group_dict(data, "total_DDD_alarm", group_key))
    summary["total_trigger_after_verification"] = safe_int(get_scalar_from_group_dict(data, "total_trigger_after_verification", group_key))
    summary["total_skip_by_verification"] = safe_int(get_scalar_from_group_dict(data, "total_skip_by_verification", group_key))

    # Rates
    summary["false_alarm_rate"] = safe_float(get_scalar_from_group_dict(data, "false_alarm_rate", group_key))
    summary["trigger_rate"] = safe_float(get_scalar_from_group_dict(data, "trigger_rate", group_key))
    summary["skip_rate"] = safe_float(get_scalar_from_group_dict(data, "skip_rate", group_key))
    summary["useless_mtd_rate"] = safe_float(get_scalar_from_group_dict(data, "useless_mtd_rate", group_key))

    # Burden metrics
    summary["fail_per_alarm"] = safe_float(get_scalar_from_group_dict(data, "fail_per_alarm", group_key))
    summary["stage_one_time_per_alarm"] = safe_float(get_scalar_from_group_dict(data, "stage_one_time_per_alarm", group_key))
    summary["stage_two_time_per_alarm"] = safe_float(get_scalar_from_group_dict(data, "stage_two_time_per_alarm", group_key))
    summary["delta_cost_one_per_alarm"] = safe_float(get_scalar_from_group_dict(data, "delta_cost_one_per_alarm", group_key))
    summary["delta_cost_two_per_alarm"] = safe_float(get_scalar_from_group_dict(data, "delta_cost_two_per_alarm", group_key))

    # Optional diagnostics
    summary["n_verify_score"] = len(get_list_from_group_dict(data, "verify_score", group_key))
    summary["n_clean_alarm_idx"] = len(get_list_from_group_dict(data, "clean_alarm_idx", group_key))
    summary["n_trigger_flag"] = len(get_list_from_group_dict(data, "trigger_after_verification", group_key))
    summary["n_skip_flag"] = len(get_list_from_group_dict(data, "skip_by_verification", group_key))

    return summary


def print_file_info(name: str, path: str, data: Dict[str, Any], group_key: str) -> None:
    print_header(name)
    print("path           :", os.path.abspath(path))
    print("modified time  :", fmt_mtime(path))
    print("tau_verify     :", data.get("tau_verify", "N/A"))
    print("group_key      :", group_key)
    print("top-level keys :", list(data.keys()))


def run_consistency_checks(
    baseline: Dict[str, Any],
    gated: Dict[str, Any],
    group_key_b: str,
    group_key_g: str,
    rtol: float,
    atol: float,
) -> None:
    print_header("CONSISTENCY CHECKS")

    # 1) group key consistency
    same_group = (group_key_b == group_key_g)
    print(f"[group_key] same? {same_group} | baseline={group_key_b} | gated={group_key_g}")

    # 2) scalar counts
    scalar_fields = [
        "total_clean_sample",
        "total_DDD_alarm",
        "false_alarm_rate",
    ]
    for field in scalar_fields:
        vb = get_scalar_from_group_dict(baseline, field, group_key_b)
        vg = get_scalar_from_group_dict(gated, field, group_key_g)
        same = approx_equal_scalar(vb, vg, rtol=rtol, atol=atol)
        print(f"[{field}] same? {same} | baseline={vb} | gated={vg}")

    # 3) list equality checks for deterministic reproducibility
    list_fields = [
        "TP_DDD",
        "clean_alarm_idx",
        "verify_score",
    ]
    for field in list_fields:
        lb = get_list_from_group_dict(baseline, field, group_key_b)
        lg = get_list_from_group_dict(gated, field, group_key_g)
        same, msg = compare_lists(lb, lg, rtol=rtol, atol=atol)
        print(f"[{field}] same? {same} | {msg}")

    # 4) sanity checks for gate logic
    trig_b = get_scalar_from_group_dict(baseline, "total_trigger_after_verification", group_key_b)
    skip_b = get_scalar_from_group_dict(baseline, "total_skip_by_verification", group_key_b)
    trig_g = get_scalar_from_group_dict(gated, "total_trigger_after_verification", group_key_g)
    skip_g = get_scalar_from_group_dict(gated, "total_skip_by_verification", group_key_g)
    ddd_b = get_scalar_from_group_dict(baseline, "total_DDD_alarm", group_key_b)
    ddd_g = get_scalar_from_group_dict(gated, "total_DDD_alarm", group_key_g)

    print("\nGate sanity:")
    print(f"baseline: total_trigger_after_verification + total_skip_by_verification = {trig_b} + {skip_b} = {safe_int(trig_b) + safe_int(skip_b)} | total_DDD_alarm = {ddd_b}")
    print(f"gated   : total_trigger_after_verification + total_skip_by_verification = {trig_g} + {skip_g} = {safe_int(trig_g) + safe_int(skip_g)} | total_DDD_alarm = {ddd_g}")


def print_summary_table(b: Dict[str, Any], g: Dict[str, Any]) -> None:
    print_header("SUMMARY TABLE (CLEAN FALSE-ALARM)")

    rows = [
        ("tau_verify", b["tau_verify"], g["tau_verify"]),
        ("total_clean_sample", b["total_clean_sample"], g["total_clean_sample"]),
        ("total_DDD_alarm", b["total_DDD_alarm"], g["total_DDD_alarm"]),
        ("total_trigger_after_verification", b["total_trigger_after_verification"], g["total_trigger_after_verification"]),
        ("total_skip_by_verification", b["total_skip_by_verification"], g["total_skip_by_verification"]),
        ("false_alarm_rate", b["false_alarm_rate"], g["false_alarm_rate"]),
        ("trigger_rate", b["trigger_rate"], g["trigger_rate"]),
        ("skip_rate", b["skip_rate"], g["skip_rate"]),
        ("useless_mtd_rate", b["useless_mtd_rate"], g["useless_mtd_rate"]),
        ("fail_per_alarm", b["fail_per_alarm"], g["fail_per_alarm"]),
        ("stage_one_time_per_alarm", b["stage_one_time_per_alarm"], g["stage_one_time_per_alarm"]),
        ("stage_two_time_per_alarm", b["stage_two_time_per_alarm"], g["stage_two_time_per_alarm"]),
        ("delta_cost_one_per_alarm", b["delta_cost_one_per_alarm"], g["delta_cost_one_per_alarm"]),
        ("delta_cost_two_per_alarm", b["delta_cost_two_per_alarm"], g["delta_cost_two_per_alarm"]),
    ]

    print(f"{'metric':<36} {'baseline':>18} {'gated':>18} {'delta(g-b)':>18}")
    print("-" * 92)
    for name, vb, vg in rows:
        try:
            delta = float(vg) - float(vb)
            delta_str = fmt_num(delta, digits=6)
        except Exception:
            delta_str = "N/A"
        print(f"{name:<36} {fmt_num(vb, 6):>18} {fmt_num(vg, 6):>18} {delta_str:>18}")


def print_reduction_table(b: Dict[str, Any], g: Dict[str, Any]) -> None:
    print_header("RELATIVE REDUCTION / CHANGE FROM BASELINE TO GATED")

    metrics_reduction = [
        "useless_mtd_rate",
        "fail_per_alarm",
        "stage_one_time_per_alarm",
        "stage_two_time_per_alarm",
        "delta_cost_one_per_alarm",
        "delta_cost_two_per_alarm",
    ]

    print(f"{'metric':<36} {'baseline':>18} {'gated':>18} {'reduction(%)':>18}")
    print("-" * 92)
    for m in metrics_reduction:
        vb = b[m]
        vg = g[m]
        red = pct_reduction(vb, vg)
        print(f"{m:<36} {fmt_num(vb, 6):>18} {fmt_num(vg, 6):>18} {fmt_num(red, 2):>18}")

    print("\nRates that are expected to move in opposite directions:")
    extra = [
        "trigger_rate",
        "skip_rate",
    ]
    print(f"{'metric':<36} {'baseline':>18} {'gated':>18} {'change(%)':>18}")
    print("-" * 92)
    for m in extra:
        vb = b[m]
        vg = g[m]
        chg = pct_change(vb, vg)
        print(f"{m:<36} {fmt_num(vb, 6):>18} {fmt_num(vg, 6):>18} {fmt_num(chg, 2):>18}")


def print_takeaways(b: Dict[str, Any], g: Dict[str, Any]) -> None:
    print_header("AUTO TAKEAWAYS")

    skip_rate = g["skip_rate"]
    trigger_rate = g["trigger_rate"]

    useless_red = pct_reduction(b["useless_mtd_rate"], g["useless_mtd_rate"])
    fail_red = pct_reduction(b["fail_per_alarm"], g["fail_per_alarm"])
    t1_red = pct_reduction(b["stage_one_time_per_alarm"], g["stage_one_time_per_alarm"])
    t2_red = pct_reduction(b["stage_two_time_per_alarm"], g["stage_two_time_per_alarm"])
    c1_red = pct_reduction(b["delta_cost_one_per_alarm"], g["delta_cost_one_per_alarm"])
    c2_red = pct_reduction(b["delta_cost_two_per_alarm"], g["delta_cost_two_per_alarm"])

    print(f"- Gated policy skips {fmt_num(skip_rate * 100.0, 2)}% of clean false-alarm-induced backend triggers.")
    print(f"- Remaining trigger rate after verification is {fmt_num(trigger_rate * 100.0, 2)}% of clean DDD alarms.")
    print(f"- useless_mtd_rate reduction: {fmt_num(useless_red, 2)}%")
    print(f"- fail_per_alarm reduction: {fmt_num(fail_red, 2)}%")
    print(f"- stage_one_time_per_alarm reduction: {fmt_num(t1_red, 2)}%")
    print(f"- stage_two_time_per_alarm reduction: {fmt_num(t2_red, 2)}%")
    print(f"- delta_cost_one_per_alarm reduction: {fmt_num(c1_red, 2)}%")
    print(f"- delta_cost_two_per_alarm reduction: {fmt_num(c2_red, 2)}%")


def main() -> None:
    args = parse_args()

    baseline = load_result(args.baseline)
    gated = load_result(args.gated)

    group_key_b = detect_group_key(baseline)
    group_key_g = detect_group_key(gated)

    print_file_info("BASELINE FILE", args.baseline, baseline, group_key_b)
    print_file_info("GATED FILE", args.gated, gated, group_key_g)

    run_consistency_checks(
        baseline=baseline,
        gated=gated,
        group_key_b=group_key_b,
        group_key_g=group_key_g,
        rtol=args.rtol,
        atol=args.atol,
    )

    summary_b = summarize(baseline, group_key_b)
    summary_g = summarize(gated, group_key_g)

    print_summary_table(summary_b, summary_g)
    print_reduction_table(summary_b, summary_g)
    print_takeaways(summary_b, summary_g)


if __name__ == "__main__":
    main()